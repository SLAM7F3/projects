#include "myinclude.h"

// ==========================================================================
// Constants
// ==========================================================================

// ==========================================================================
int main(int argc, char *argv[])
// ==========================================================================
{
   const int istart=3;
   const int istop=4101;
   
   int i,j,r,nfactors,k;
   int factor[20];

   for (i=istart; i<istop; i += 2)
   {
      r=i;
      nfactors=0;
      j=2;
      while (j<mytrunc(i/2)+1)
      {
         if (r%j==0)
         {
            factor[nfactors++]=j;
            r /= j;
         }
         else
         {
            j++;
         }
      } // while factorization loop

      cout << "Prime factors of i = " << i << " : " ;
      for (k=0; k<nfactors; k++)
      {
         cout << factor[k] << "  ";
      }
      outputfunc::newline();
   } // loop over number i 
}






